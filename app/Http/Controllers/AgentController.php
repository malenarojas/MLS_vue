        public function storedocumentacion(string $agentid, Request $request)
        {
            // ğŸ” LOG antes de la validaciÃ³n para depuraciÃ³n
            Log::info("ğŸ“¥ Logs recibidos antes de validar:", [
                'archivo' => $request->file('archivo'),
                'input_files' => $request->input('archivo', []),
            ]);

            // âœ… ValidaciÃ³n correcta
            $request->validate([
                'archivo' => 'required|array',
                'archivo.*' => 'required|file|mimes:pdf,jpeg,png,jpg,gif|max:2048',
                'descriptions' => 'nullable|array',
                'descriptions.*' => 'nullable|string',
                'types' => 'nullable|array',
                'types.*' => 'required|exists:documentation_types,id',
            ]);

            // âŒ ERROR en `$request->has('files')`, corregido a `archivo`
            if (!$request->hasFile('archivo')) {
                Log::error("ğŸš¨ No se detectaron archivos en la request.");
                return response()->json(['message' => 'No files received'], 400);
            }

            // âœ… Obtener archivos correctamente
            $files = $request->file('archivo');
            $descriptions = $request->input('descriptions', []);
            $types = $request->input('types', []);

            Log::info("ğŸ“‚ Archivos despuÃ©s de la validaciÃ³n:", ['files' => $files]);

            $documentListingData = [];

            foreach ($files as $index => $file) {
                $description = $descriptions[$index] ?? null;
                $typeId = $types[$index] ?? null;

                if (!$file || !$file->isValid()) {
                    Log::error("âŒ Archivo no vÃ¡lido en el Ã­ndice $index", ['file' => $file]);
                    continue;
                }

                Log::info("ğŸ“‚ Procesando archivo: {$file->getClientOriginalName()}");

                // âœ… Guardar el archivo correctamente
                $fileName = $this->imageService->uploadImageFromFile($file, self::DOCUMENTS_DIRECTORY);

                if (!$fileName) {
                    Log::error("âš  Error al subir el archivo: {$file->getClientOriginalName()}");
                    continue;
                }

                Log::info("âœ… Archivo guardado correctamente en: " . self::DOCUMENTS_DIRECTORY . '/' . $fileName);

                // âœ… Guardar en la base de datos
                $document = Documentation::create([
                    'link' =>  $fileName,
                    'description' => $description,
                    'documentation_type_id' => $typeId,
                    'original_name' => $file->getClientOriginalName(),
                ]);

                Log::info("ğŸ“„ Documento guardado en la base de datos con ID: " . $document->id);

                $documentListingData[] = [
                    'documentation_id' => $document->id,
                    'agent_id' => $agentid,
                    'created_at' => now(),
                    'updated_at' => now(),
                ];
            }

            // âœ… Insertar en la tabla de relaciÃ³n si hay documentos
            if (!empty($documentListingData)) {
                DB::table('agents_documentations')->insert($documentListingData);
                Log::info("âœ… Se insertaron los documentos en agents_documentations.");
            } else {
                Log::warning("âš  No se insertaron documentos en agents_documentations.");
            }

            return response()->json([
                'message' => 'Files uploaded and documents created successfully',
            ], 201);
        }


    private function saveAuditLogs(array $logs, int $agentId, ?int $userId)
